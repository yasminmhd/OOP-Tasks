public class Formula {
    public static double temperatureFormula(double quantity){
        return(quantity*9/5)+32;
    }    
    
    public static double massFormula(double quantity){
        return quantity * 2.2046;
    }
    
    public static double distanceFormula(double quantity){
        return quantity * 0.621371;
    }

    
}
