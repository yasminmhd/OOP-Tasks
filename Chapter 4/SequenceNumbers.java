package sequencenumbers;
//class name SequenceNumbers
public class SequenceNumbers {
    public static void main(String[] args) {
        
        //outer loop initialization,condition,decrement
        for(int i=9;i>=1;i--){
            //inner loop
            for(int j=2;j>=1;j--){
                //print the numbers
                System.out.print(i);
            }System.out.print(" ");//print the spaces between the numbers
        }
    }}
